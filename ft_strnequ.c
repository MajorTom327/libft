#include "libft.h"

int	ft_strnequ(char const *s1, char const *s2, size_t len)
{
	size_t i;

	i = 0;
	while (i != len && s1[i] != '\0' && s2[i] != '\0')
	{
		if (s1[i] != s2[i])
			return (0);
		i++;
	}
	if (s1[i] != s2[i])
		return (0);
	return (1);
}
